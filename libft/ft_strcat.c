#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	lens1;
	int		i;

	i = 0;
	lens1 = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[lens1] = s2[i];
		lens1++;
		i++;
	}
	s1[lens1] = '\0';
	return (s1);
}
