#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	size_t	len;
	char	*str;

	if (s != NULL)
	{
		i = 0;
		len = ft_strlen(s);
		if ((str = (char*)malloc(sizeof(*str) * (len + 1))) == NULL)
			return (NULL);
		while (s[i] != '\0')
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
