#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (*s != '\0')
	{
		s++;
		i++;
	}
	while (i >= 0 && *s != c)
	{
		s--;
		i--;
	}
	if (*s == c)
		return ((char*)s);
	return (NULL);
}
